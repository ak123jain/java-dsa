import java.util.Scanner;
// print the decreasing n
public class DecreasingNo {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter teh no");
        int n = sc.nextInt();
        for(int num = n ; num>=1 ; num--){// num vo ha jo ham print karna cha rahe ha to
            System.out.println(num);
        }
        }
    }// num vo ha jo ham print karna cha rahe ha to hma  shuru n sa karna ha  or num ki value 1 sa kam nahi honi chahiya  varna - ma chali jyagi
